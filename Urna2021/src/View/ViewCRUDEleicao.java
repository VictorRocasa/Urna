    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int indice = this.tblEleicoes.getSelectedRow();
        if(indice !=-1){
            Eleicao eleicao = (Eleicao)eleicoes.get(indice);
            ViewCadastroEleicao viewCadastroEleicao = ViewCadastroEleicao.getInstancia();
            viewCadastroEleicao.setModoEdicao(eleicao);
            this.dispose();
            viewCadastroEleicao.setVisible(true);
        }
        else
            JOptionPane.showMessageDialog(null,"Aviso, selecione uma eleição para ser editada!","Aviso!", 2);    
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int indice = this.tblEleicoes.getSelectedRow();
        if(indice !=-1){
            Eleicao eleicao = (Eleicao)eleicoes.get(indice);
            Object[] options = {"Confirmar", "Cancelar"};
            if(JOptionPane.showOptionDialog(null, "Deseja mesmo excluir a eleição para "+eleicao.getCargo().toLowerCase()+","+eleicao.getTurno().toLowerCase()+" turno, planejada para o dia "+eleicao.getDataEleicao()+" na região de "+eleicao.getRegiao()+"?",
            "Aviso", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, options, options[1]) == 1)
                return;
            EleicaoDAO eleicaoDAO = EleicaoDAO.getInstancia();
            eleicaoDAO.exclui(eleicao.getIdEleicao());
            preencheTabela();
        }
        else
            JOptionPane.showMessageDialog(null,"Aviso, selecione uma eleição para ser excluida!","Aviso!", 2);    
    }//GEN-LAST:event_btnExcluirActionPerformed

