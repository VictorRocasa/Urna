    public void preencheTabela(){
        String[] colunas = {"Cargo","Região","Data","Turno"};
        DefaultTableModel model = new DefaultTableModel(colunas,0);
        EleicaoDAO eleicaoDAO = EleicaoDAO.getInstancia();
        eleicoes = eleicaoDAO.consulta(null, 0);
        Eleicao eleicao;
        for(int i = 0; i < eleicoes.size(); i++){
            eleicao = (Eleicao)eleicoes.get(i);
            Object[] linha = {eleicao.getCargo(), eleicao.getRegiao(),eleicao.getDataEleicao(), eleicao.getTurno()};
            model.addRow(linha);
        }
        this.tblEleicoes.setModel(model);
    }
    
