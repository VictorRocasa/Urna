    public boolean validaCampos(){
        String numero = this.ftxtNumero.getText();
        numero = numero.replace(" ", "");
        String nome = this.txtNome.getText();
        nome = nome.replace(" ", "");
        
        if(numero.equals("")){
            JOptionPane.showMessageDialog(null, "O campo número não deve ficar em branco!","Erro!", 2);
            return false;
        }
        if(nome.equals("")){
            JOptionPane.showMessageDialog(null, "O nome não deve ficar em branco!","Erro!!", 2);
            return false;
        }
        
        if(!numero.matches("[0-9]{2,5}")){
            JOptionPane.showMessageDialog(null, "Número inválido!","Erro!", 2);
            return false;
        }
        if(!nome.matches("[A-Za-záàâãéèêíïóôõöúçñÁÀÂÃÉÈÍÏÓÔÕÖÚÇÑ]{3,}")){
            JOptionPane.showMessageDialog(null, "Nome inválido!","Erro!!", 2);
            return false;
        }
        if(Integer.parseInt(numero) < 10){
            JOptionPane.showMessageDialog(null, "Número inválido!","Erro!!", 2);
            return false; 
        }
        return true;
    }
    
    public void limpaCampos(){
        this.ftxtNumero.setText("");
        this.txtNome.setText("");
    }
    
    void setModoCriacao() {
        editar = false;
        id_edicao = -1;
        this.limpaCampos();
    }
    
    void setModoEdicao(Partido partido) {
        editar = true;
        id_edicao = partido.getIdPartido();
        this.ftxtNumero.setText(""+partido.getNumero());
        this.txtNome.setText(partido.getNome());
    }

    
