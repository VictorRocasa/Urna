    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        if(!validaCampos())
            return;
        Partido partido = new Partido();
        partido.setNumero(Integer.parseInt(this.ftxtNumero.getText()));
        partido.setNome(this.txtNome.getText());
        PartidoDAO partidoDAO = PartidoDAO.getInstancia();
        ArrayList<Object> partidos = partidoDAO.consulta(partido, 2);
        if(partidos!=null)
            if(partidos.isEmpty()){
                if(editar){
            partido.setIdPartido(id_edicao);
                    if(partidoDAO.altera(partido)){
                        ViewCRUDPartido viewCRUDPartido = ViewCRUDPartido.getInstancia();
                        viewCRUDPartido.preencheTabela();
                        JOptionPane.showMessageDialog(null,"Cadastro alterado com sucesso.","Sucesso!", 1);
                        this.dispose();
                        viewCRUDPartido.setVisible(true);          
                    }
                }
                else
                    if(partidoDAO.adiciona(partido)){
                        this.limpaCampos();
                        Object[] options = {"Sim", "Não"};
                        if(JOptionPane.showOptionDialog(null, "Cadastro realizado com sucesso! Deseja realizar outro cadastro?","Sucesso", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, options, options[0]) == 0)
                            return;
                        else{
                            this.dispose();
                            ViewCRUDPartido viewCRUDPartido = ViewCRUDPartido.getInstancia();
                            viewCRUDPartido.preencheTabela();
                            viewCRUDPartido.setVisible(true);
                        }        
                    }
            }
            else
                JOptionPane.showMessageDialog(null,"Número já cadastrado!.","Erro!", 2);
    }//GEN-LAST:event_btnConfirmarActionPerformed

