    public boolean validaCampos(){
        String cpf = this.ftxtCPF.getText();
        cpf = cpf.replace(" ", "");
        String numero = this.ftxtNumero.getText();
        numero = numero.replace(" ", "");
        String cargo = this.txtCargo.getText();
        cargo = cargo.replace(" ", "");
        
        if(cpf.equals("...")){
            JOptionPane.showMessageDialog(this, "O cpf não deve ficar em branco!","Erro!", 2);
            return false;
        }
        if(numero.equals("")){
            JOptionPane.showMessageDialog(this, "O campo número não deve ficar em branco!","Erro!", 2);
            return false;
        }
        if(cargo.equals("")){
            JOptionPane.showMessageDialog(this, "O cargo não deve ficar em branco!","Erro!!", 2);
            return false;
        }
        if(!partidarioController.validarCPF(cpf)){
            JOptionPane.showMessageDialog(this, "cpf inválido!","Erro!", 2);
            return false;
        }
        if(!numero.matches("[0-9]{2,5}")){
            JOptionPane.showMessageDialog(this, "Número inválido!","Erro!", 2);
            return false;
        }
        if(!cargo.matches("[A-Za-záàâãéèêíïóôõöúçñÁÀÂÃÉÈÍÏÓÔÕÖÚÇÑ]{3,}")){
            JOptionPane.showMessageDialog(this, "Cargo inválido!","Erro!!", 2);
            return false;
        }
        return true;
    }
    
