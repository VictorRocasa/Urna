    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        if(!this.validaCampos())
            return;
        if(idEdicao==-1){
            try {
                partidarioController.adicionar(this.ftxtCPF.getText(), Integer.parseInt(this.ftxtNumero.getText()), this.txtCargo.getText());
                this.limpaCampos();
                Object[] options = {"Sim", "Não"};
                if(JOptionPane.showOptionDialog(null, "Cadastro realizado com sucesso! Deseja realizar outro cadastro?","Sucesso", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, options, options[0]) == 0)
                    return;
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this,"Erro ao adicionar o registro, tente novamente mais tarde!","Erro!", 0);
                return;
            }catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this,"Eleitor com o CPF digitado inexistente!","Erro!", 2);
                return;
            } catch (NegativeArraySizeException ex) {
                JOptionPane.showMessageDialog(this,"Partidário com esse CPF já existe!","Erro!", 2);
                return;
            } catch (ArithmeticException ex) {
                JOptionPane.showMessageDialog(this,"Partido com o número digitado inexistente!","Erro!", 2);
                return;
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ViewCadastroPartidario.class.getName()).log(Level.SEVERE, null, ex);
                return;
            } 
        }
        else{
            try {
                partidarioController.alterarPorId(idEdicao, Integer.parseInt(this.ftxtNumero.getText()), this.txtCargo.getText());
                JOptionPane.showMessageDialog(this,"Cadastro alterado com sucesso.","Sucesso!", 1);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this,"Erro ao alterar o registro, tente novamente mais tarde!","Erro!", 0);
                return;
            }catch (ArithmeticException ex) {
                JOptionPane.showMessageDialog(this,"Partido com o número digitado inexistente!","Erro!", 2);
                return;
            }catch (ClassNotFoundException ex) {
                Logger.getLogger(ViewCadastroPartidario.class.getName()).log(Level.SEVERE, null, ex);
                return;
            } 
        }
        this.dispose();
        ViewCRUDPartidario viewCRUDPartidario = new ViewCRUDPartidario(partidarioController);
        viewCRUDPartidario.preencheTabela();
        viewCRUDPartidario.setVisible(true);
    }//GEN-LAST:event_btnConfirmarActionPerformed

