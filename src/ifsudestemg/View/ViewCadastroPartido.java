    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        if(!validaCampos())
            return;
        if(idEdicao==-1){
            try {
                partidoController.adicionar(Integer.parseInt(this.ftxtNumero.getText()),this.txtNome.getText());
                this.limpaCampos();
                Object[] options = {"Sim", "Não"};
                if(JOptionPane.showOptionDialog(null, "Cadastro realizado com sucesso! Deseja realizar outro cadastro?","Sucesso", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, options, options[0]) == 0)
                    return;
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this,"Erro ao adicionar o registro, tente novamente mais tarde!","Erro!", 0);
                return;
            } catch (NumberFormatException ex2) {
                JOptionPane.showMessageDialog(this,"Partido com esse número já existente!","Erro!", 2);
                return;
            } catch (NegativeArraySizeException ex3) {
                JOptionPane.showMessageDialog(this,"Partido com esse nome já existente!","Erro!", 2);
                return;
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this,"A primeira e a segunda senha não conferem!","Erro!", 0);
            }    
        }
        else{
            try {
                partidoController.alterarPorId(idEdicao, Integer.parseInt(this.ftxtNumero.getText()), this.txtNome.getText());
                JOptionPane.showMessageDialog(this,"Cadastro alterado com sucesso.","Sucesso!", 1);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this,"Erro ao alterar o registro, tente novamente mais tarde!","Erro!", 0);
                return;
            } catch (NumberFormatException ex2) {
                JOptionPane.showMessageDialog(this,"Partido com esse número já existente!","Erro!", 2);
                return;
            } catch (NegativeArraySizeException ex3) {
                JOptionPane.showMessageDialog(this,"Partido com esse nome já existente!","Erro!", 2);
                return;
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this,"A primeira e a segunda senha não conferem!","Erro!", 0);
            }      
        }
        this.dispose();
        origem.setVisible(true);
    }//GEN-LAST:event_btnConfirmarActionPerformed

