    public boolean validaCampos(){
        String numero = this.ftxtNumero.getText();
        numero = numero.replace(" ", "");
        String nome = this.txtNome.getText();
        nome = nome.replace(" ", "");
        
        if(numero.equals("")){
            JOptionPane.showMessageDialog(this, "O campo número não deve ficar em branco!","Erro!", 2);
            return false;
        }
        if(nome.equals("")){
            JOptionPane.showMessageDialog(this, "O nome não deve ficar em branco!","Erro!!", 2);
            return false;
        }
        
        if(!numero.matches("[0-9]{2,5}")){
            JOptionPane.showMessageDialog(this, "Número inválido!","Erro!", 2);
            return false;
        }
        if(!nome.matches("[A-Za-záàâãéèêîíïóôõöúçñÁÀÂÃÉÈÍÏÓÔÕÖÚÇÑÎ]{3,200}")){
            JOptionPane.showMessageDialog(this, "Nome inválido!","Erro!!", 2);
            return false;
        }
        if(Integer.parseInt(numero) < 10){
            JOptionPane.showMessageDialog(this, "Número inválido!","Erro!!", 2);
            return false; 
        }
        return true;
    }
    
