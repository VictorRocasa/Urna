package ifsudestemg.Model;

/**
 *
 * @author victorPC
 */
public class Candidato {
    private int idCandidato;
    private int idLegenda;
    private int idPartidario;
    private int numero;

    public int getIdCandidato() {
        return idCandidato;
    }

    public void setIdCandidato(int idCandidato) {
        this.idCandidato = idCandidato;
    }

    public int getIdLegenda() {
        return idLegenda;
    }

    public void setIdLegenda(int idLegenda) {
        this.idLegenda = idLegenda;
    }

    public int getIdPartidario() {
        return idPartidario;
    }

    public void setIdPartidario(int idPartidaro) {
        this.idPartidario = idPartidaro;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }
    
}
