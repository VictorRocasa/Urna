    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int indice = this.tblPartidos.getSelectedRow();
        if(indice !=-1){
            ViewCadastroPartido viewCadastroPartido = new ViewCadastroPartido(partidoController,((int)model.getValueAt(indice, 0)),(""+model.getValueAt(indice, 1)),(""+model.getValueAt(indice, 2)),-1);
            this.dispose();
            viewCadastroPartido.setVisible(true);
        }
        else
            JOptionPane.showMessageDialog(this,"Aviso, selecione um partido para ser editado!","Aviso!", 2);
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int indice = this.tblPartidos.getSelectedRow();
        if(indice !=-1){
            Object[] options = {"Confirmar", "Cancelar"};
            if(JOptionPane.showOptionDialog(this, "Deseja mesmo excluir o partido "+model.getValueAt(indice, 2)+","+model.getValueAt(indice, 1)+"?",
                "Aviso", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, options, options[1]) == 0){
                try{
                    partidoController.excluirPorId((int) model.getValueAt(indice, 0));
                    JOptionPane.showMessageDialog(this,"Deletado com sucesso!","Sucesso!", 1);
                    preencheTabela();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this,"Erro ao excluir o registro, tente novamente mais tarde!","Erro!", 0);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(ViewCRUDPartido.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        else
        JOptionPane.showMessageDialog(this,"Aviso, selecione uma elei√ß√£o para ser excluida!","Aviso!", 2);
    }//GEN-LAST:event_btnExcluirActionPerformed

